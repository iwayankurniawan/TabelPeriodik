public class DaftarNamaUnsur {
    private int GambarUnsur;
    private String NamaUnsur;

    public DaftarNamaUnsur(int GambarUnsur, String NamaUnsur) {
        this.GambarUnsur = GambarUnsur;
        this.NamaUnsur = NamaUnsur;
    }

    public int getGambarUnsur() {
        return this.GambarUnsur;
    }

    public String getNamaUnsur() {
        return this.NamaUnsur;
    }

}
